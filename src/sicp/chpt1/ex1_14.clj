(ns sicp.chpt1.ex1-14
  (:require [clojure.tools.trace :as t]))


;;; Each call to `cc` results in 2 more calls to `cc`, also `amount` decrements by 1 when `kinds of coins` is 1.
;;; Therefore, the upper bound time complexity is \\(O(2^{amount})\\).

;;; Since the maximum recursive depth is when `kinds-of-coins` is 1. the space complexity is \\(O(amount)\\)

(defn first-denomination
  [kinds-of-coins]
  (cond (= kinds-of-coins 1) 1
        (= kinds-of-coins 2) 5
        (= kinds-of-coins 3) 10
        (= kinds-of-coins 4) 25
        (= kinds-of-coins 5) 50))


(t/deftrace cc
  [amount kinds-of-coins]
  (cond (= amount 0) 1
        (or (< amount 0) (= kinds-of-coins 0)) 0
        :else
        (+ (cc amount
               (- kinds-of-coins 1))
           (cc (- amount
                  (first-denomination kinds-of-coins))
               kinds-of-coins))))

(defn count-change
  [amount]
  (cc amount 5))

